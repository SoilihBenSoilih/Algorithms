const pathFinder = require('../src/maze');

test('Exercice1:', () => {
    let input = [
        "#S.......#",
        "##.#####.#",
        "##.......#",
        "##########"
    ];
    let expected = [
        "#01234567#",
        "##2#####8#",
        "##3456789#",
        "##########"
    ];
    expect(pathFinder(input)).toEqual(expected);


    input = [
        "#.########",
        "#.##..####",
        "..##..#...",
        "####..#S##",
        "#....#####"
    ];
    expected = [
        "#7########",
        "#6##EF####",
        "45##DE#123",
        "####CD#0##",
        "#89AB#####"
    ];
    expect(pathFinder(input)).toEqual(expected);

    input = [
        "...............",
        "......#........",
        "...............",
        "...............",
        "...............",
        "............#..",
        "............#..",
        "...............",
        "S..............",
        ".#............."
    ];
    expected = [
        "234567899876543",
        "345678#AA987654",
        "456789ABBA98765",
        "56789ABCCBA9876",
        "456789ABBA98765",
        "3456789AA987#54",
        "234567899876#43",
        "123456788765432",
        "012345677654321",
        "1#3456788765432"
    ];
    expect(pathFinder(input)).toEqual(expected);
});
